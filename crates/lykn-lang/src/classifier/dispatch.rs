pub fn is_surface_form(name: &str) -> bool {
    matches!(
        name,
        "func"
            | "bind"
            | "match"
            | "type"
            | "obj"
            | "cell"
            | "express"
            | "swap!"
            | "reset!"
            | "set!"
            | "->"
            | "->>"
            | "some->"
            | "some->>"
            | "if-let"
            | "when-let"
            | "fn"
            | "lambda"
            | "conj"
            | "assoc"
            | "dissoc"
            | "macro"
            | "import-macros"
            | "="
            | "!="
            | "and"
            | "or"
            | "not"
    )
}

